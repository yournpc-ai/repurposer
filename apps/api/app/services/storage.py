def owner_from_path(key: str | None) -> str | None:
    """Extract the owning user id (or 'demo') from a stored object key."""
    if not key:
        return None
    first = key.strip("/").split("/", 1)[0]
    return first if first else None


def _is_not_found(e: ClientError) -> bool:
    """True when the S3 error means the object simply does not exist."""
    return e.response.get("Error", {}).get("Code") in ("404", "NoSuchKey", "NotFound")


async def exists(key: str | None) -> bool:
    """Return True if the object exists in storage.

    Only a missing object yields False; credential/network errors propagate so
    callers (e.g. seed reconcilers) do not silently misread them as "absent".
    """
    if not key:
        return False
    client = _get_s3_client()
    try:
        await asyncio.to_thread(
            client.head_object,
            Bucket=settings.s3_bucket_name,
            Key=_normalize_key(key),
        )
        return True
    except ClientError as e:
        if _is_not_found(e):
            return False
        raise


async def size(key: str | None) -> int | None:
    """Return the object size in bytes, or None if missing (404 only)."""
    if not key:
        return None
    client = _get_s3_client()
    try:
        response = await asyncio.to_thread(
            client.head_object,
            Bucket=settings.s3_bucket_name,
            Key=_normalize_key(key),
        )
        return int(response["ContentLength"])
    except ClientError as e:
        if _is_not_found(e):
            return None
        raise


def resolve_file_path(key: str | None) -> Path | None:
    """Compatibility alias: download an object to a temp file.

    Legacy callers expected a local Path under asset_dir. Under S3-only storage
    we download to a temp file and return that path. The caller must delete it.
    """
    # download_to_temp is async; this synchronous wrapper is intentionally
    # limited. Callers should migrate to await download_to_temp(key).
    if not key:
        return None

    try:
        return asyncio.run(download_to_temp(key))
    except RuntimeError:
        return None


def file_to_data_url(path_or_key: str | Path | None) -> str | None:
    """Return a base64 data URL for a local file or an object key.

    Accepts either a legacy Path or an object key. For object keys, the bytes
    are fetched from storage synchronously (best-effort for legacy sync callers).
    """
    if path_or_key is None:
        return None

    raw: bytes | None = None
    mime = "application/octet-stream"

    if isinstance(path_or_key, Path):
        path = path_or_key
        if not path.is_file():
            return None
        try:
            raw = path.read_bytes()
        except OSError:
            return None
        guessed, _ = mimetypes.guess_type(str(path))
        if guessed:
            mime = guessed
    else:
        key = str(path_or_key)
        try:
            raw = asyncio.run(read(key))
        except Exception:
            return None
        guessed, _ = mimetypes.guess_type(key)
        if guessed:
            mime = guessed

    if raw is None:
        return None
    encoded = base64.b64encode(raw).decode("ascii")
    return f"data:{mime};base64,{encoded}"
