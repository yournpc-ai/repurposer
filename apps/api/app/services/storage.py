async def presign_upload(
    key: str,
    content_type: str | None = None,
    *,
    ttl: int | None = None,
) -> str:
    """Generate a presigned PUT URL for direct upload to object storage."""
    client = _get_s3_client()
    params: dict[str, str] = {
        "Bucket": settings.s3_bucket_name,
        "Key": _normalize_key(key),
    }
    if content_type:
        params["ContentType"] = content_type
    expires_in = ttl if ttl is not None else settings.s3_presign_upload_ttl
    return await asyncio.to_thread(
        client.generate_presigned_url,
        "put_object",
        Params=params,
        ExpiresIn=expires_in,
    )


async def presign_download(
    key: str,
    *,
    filename: str | None = None,
    ttl: int = 300,
) -> str:
    """Generate a presigned GET URL for an object.

    When ``filename`` is given, the response carries
    ``Content-Disposition: attachment; filename*=UTF-8''<name>`` so the browser
    downloads instead of playing inline (the plain public URL cannot do this).
    """
    client = _get_s3_client()
    params: dict[str, str] = {
        "Bucket": settings.s3_bucket_name,
        "Key": _normalize_key(key),
    }
    if filename:
        from urllib.parse import quote

        params["ResponseContentDisposition"] = (
            f"attachment; filename*=UTF-8''{quote(filename)}"
        )
    return await asyncio.to_thread(
        client.generate_presigned_url,
        "get_object",
        Params=params,
        ExpiresIn=ttl,
    )


async def save(key: str, data: bytes | BinaryIO, content_type: str | None = None) -> str:
    """Upload bytes or a file-like object to object storage and return the key."""
    client = _get_s3_client()
    body = data if isinstance(data, bytes) else data.read()
    params: dict[str, object] = {
        "Bucket": settings.s3_bucket_name,
        "Key": _normalize_key(key),
        "Body": body,
    }
    if content_type:
        params["ContentType"] = content_type
    await asyncio.to_thread(client.put_object, **params)
    return key


async def save_upload(
    file_obj: BinaryIO,
    project_id: UUID,
    user_id: UUID | str,
    filename: str,
) -> str:
    """Save uploaded file to project storage and return the object key."""
    key = await get_upload_path(project_id, user_id, filename)
    return await save(key, file_obj)


async def save_speaker_upload(
    file_obj: BinaryIO,
    speaker_id: UUID,
    user_id: UUID | str,
    filename: str,
) -> str:
    """Save uploaded file to speaker storage and return the object key."""
    key = await get_speaker_upload_path(speaker_id, user_id, filename)
    return await save(key, file_obj)


async def save_brand_media_upload(
    file_obj: BinaryIO,
    user_id: UUID | str,
    filename: str,
) -> str:
    """Save an intro/outro image or video and return its object key."""
    key = await get_brand_media_path(user_id, filename)
    return await save(key, file_obj)


async def save_output(
    project_id: UUID,
    user_id: UUID | str,
    filename: str,
    content: bytes,
) -> str:
    """Save output bytes to project storage and return the object key."""
    key = await get_output_path(project_id, user_id, filename)
    return await save(key, content)


async def read(key: str) -> bytes:
    """Read an object's bytes from storage."""
    client = _get_s3_client()
    response = await asyncio.to_thread(
        client.get_object,
        Bucket=settings.s3_bucket_name,
        Key=_normalize_key(key),
    )
    return await asyncio.to_thread(response["Body"].read)


async def download_to_temp(key: str | None) -> Path | None:
    """Download an object to a temporary file and return its path.

    Streams to disk via ``download_file`` (no full-object memory buffering), so
    it is safe for multi-GB source videos. The caller is responsible for
    deleting the temp file.
    """
    if not key:
        return None
    client = _get_s3_client()
    suffix = Path(key).suffix or ""
    fd, tmp_path = tempfile.mkstemp(suffix=suffix)
    os.close(fd)
    try:
        await asyncio.to_thread(
            client.download_file,
            settings.s3_bucket_name,
            _normalize_key(key),
            tmp_path,
        )
        return Path(tmp_path)
    except Exception:
        Path(tmp_path).unlink(missing_ok=True)
        raise


async def delete(key: str | None) -> None:
    """Delete a single object by key."""
    if not key:
        return
    client = _get_s3_client()
    await asyncio.to_thread(
        client.delete_object,
        Bucket=settings.s3_bucket_name,
        Key=_normalize_key(key),
    )


async def delete_file(key: str | None) -> None:
    """Alias for delete(), matching the legacy storage API."""
    await delete(key)


async def _list_keys(prefix: str) -> list[str]:
    """List all object keys under a prefix."""
    client = _get_s3_client()
    paginator = client.get_paginator("list_objects_v2")
    keys: list[str] = []
    for page in await asyncio.to_thread(
        lambda: list(
            paginator.paginate(Bucket=settings.s3_bucket_name, Prefix=prefix)
        ),
    ):
        for obj in page.get("Contents", []):
            keys.append(obj["Key"])
    return keys


async def delete_prefix(prefix: str) -> None:
    """Delete all objects under a prefix."""
    keys = await _list_keys(prefix)
    if not keys:
        return
    client = _get_s3_client()
    # S3 delete_objects accepts up to 1000 keys per call.
    for i in range(0, len(keys), 1000):
        batch = [{"Key": k} for k in keys[i : i + 1000]]
        await asyncio.to_thread(
            client.delete_objects,
            Bucket=settings.s3_bucket_name,
            Delete={"Objects": batch},
        )


async def delete_project_files(project_id: UUID, user_id: UUID | str) -> None:
    """Delete all files for a project."""
    await delete_prefix(get_project_upload_dir(project_id, user_id))
    await delete_prefix(get_project_output_dir(project_id, user_id))


async def delete_speaker_files(speaker_id: UUID, user_id: UUID | str) -> None:
    """Delete all files for a speaker."""
    await delete_prefix(get_speaker_upload_dir(speaker_id, user_id))


