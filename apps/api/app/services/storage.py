def public_url(key: str | None) -> str | None:
    """Return the public read URL for an object key."""
    if not key:
        return None
    base = settings.s3_public_url.rstrip("/")
    return f"{base}/{_normalize_key(key)}"


def resolve_stored_url(value: str | None) -> str | None:
    """Resolve a stored URL-or-key value to a browser-usable URL.

    DB columns hold bare object keys (new data) or legacy ``/api/v1/...`` paths
    (rows from before the object-storage migration). Bare keys are joined with
    the public URL base; absolute URLs and legacy API paths pass through
    unchanged so old rows keep working through the redirect endpoints.
    """
    if not value:
        return value
    if value.startswith(("http://", "https://", "/")):
        return value
    return public_url(value)


def stream_url(key: str | None) -> str | None:
    """Browser-playable URL for an uploaded source file."""
    return public_url(key)


def output_url(key: str | None) -> str | None:
    """Browser-playable URL for a rendered output."""
    return public_url(key)


def music_url(mood: str | None) -> str | None:
    """Legacy mood-track endpoint URL (still served by /api/v1/music/{mood})."""
    if not mood:
        return None
    return f"/api/v1/music/{mood}"


def music_stream_url(music_id: UUID | str | None) -> str | None:
    """Stream URL for a Music row (served by /api/v1/music/{music_id}/stream)."""
    if music_id is None or str(music_id) == "":
        return None
    return f"/api/v1/music/{music_id}/stream"


